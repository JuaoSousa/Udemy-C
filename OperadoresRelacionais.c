/*
           Operadores Relacionais
> - Maior que ; Ex: 6 > 2 =            verdadeiro
< - Menor que ; Ex: 6 < 2 =            falso
>= - Maior ou igual que ; Ex: 6 >= 2 = verdadeiro
<= - Menor ou igual que ; Ex: 6 <= 2 = falso
== - Igual a ; Ex: 6 == 2 =            falso
!= - Diferente de ; Ex: 6 != 2 =       verdadeiro
*/